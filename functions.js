const add2Numbers = (x, y) => {
  return x + y;
};
const substract2Numbers = (x, y) => {
  return x - y;
};
const multiply2Numbers = (x, y) => {
  return x * y;
};
const divide2Numbers = (x, y) => {
  return x / y;
};

module.exports = {
  add2Numbers,
  substract2Numbers,
  multiply2Numbers,
  divide2Numbers
};
